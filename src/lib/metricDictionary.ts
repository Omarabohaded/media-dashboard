export type MetricDefinition = (typeof metricDictionary.metrics)[number];
export type SourceMappingDefinition = (typeof metricDictionary.sourceMapping)[number];
export type CustomSignalDefinition = (typeof metricDictionary.customSignals)[number];

export function getMetricById(id: string) {
  return metricDictionary.metrics.find((metric) => metric.id === id);
}

export function getMetricsByCategory(category: string) {
  return metricDictionary.metrics.filter((metric) => metric.category === category);
}

export function getMustHaveMetrics() {
  return metricDictionary.metrics.filter((metric) => metric.priority === "Must Have");
}

export function getMetricsRequiringWebsiteTruth() {
  return metricDictionary.metrics.filter((metric) =>
    metric.source.includes("Website") ||
    metric.scopeType.includes("Website") ||
    metric.scopeType.includes("store")
  );
}
